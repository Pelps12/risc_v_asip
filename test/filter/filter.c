/*
    r1: [data0, data1, data2, data3]
    r2: [coef0, coef1]
    data[0]: [X, data0, coef0] (24 bits)
*/
unsigned char filter(


    unsigned char *data, /**Data in RGB - 8 bits*/ 
    short *coef /** filter coefficient - 16 bits*/)
{
#if defined(ACCEL_FILT) && defined(__riscv)
    unsigned char result;
    asm volatile(
        ".insn i 0x0B, 0, %0, 0(%1)\n"
        : "=r"(result)
        : "r"(data)
        : "memory"
    );
    return result;
#else
    int sum;
    unsigned char result ;
    unsigned char i ;
    sum = 0 ;
    /*--- Multiply accumulate data ------------*/
    //Cyber unroll_times=all
    for(i=0;i<SIZE;i++){
        sum += data[i] * coef[i] ;
    }
    /*--- Saturation ----------------------------------*/
    if ( sum < 0 ){
        sum = 0 ;
    } else if ( sum > ( 255 << 10 )){
        sum = (255 << 10);
    }
    /*--- 8-bit conversion----------------*/
    result = ( sum >> 10 ) & 0xff ;
    return result;
#endif
}

